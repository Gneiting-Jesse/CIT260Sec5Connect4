public class SummationOfArrays {
    
    public static void main(String[] args) {
        double[] nums = {151.2, 43.3, 12.8};
        double sum = 0;
        for (double x : nums) {
             sum += x;
}
        System.out.println("The summation of the array is "+sum);
    }
    
}
